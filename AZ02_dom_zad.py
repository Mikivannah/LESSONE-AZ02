import pandas as pd
import numpy as np

# Создаем DataFrame с данными
data = {
    'Ученик': ['Ученик1', 'Ученик2', 'Ученик3', 'Ученик4', 'Ученик5',
               'Ученик6', 'Ученик7', 'Ученик8', 'Ученик9', 'Ученик10'],
    'Математика': [85, 78, 92, 88, 76, 81, 95, 89, 84, 77],
    'Физика':     [90, 85, 95, 80, 70, 88, 92, 84, 86, 75],
    'Химия':      [78, 82, 85, 88, 80, 79, 86, 83, 84, 77],
    'Биология':   [82, 79, 88, 85, 77, 80, 92, 87, 81, 76],
    'История':    [80, 75, 89, 84, 78, 82, 85, 83, 79, 77]
}

df = pd.DataFrame(data)

# Вывод первых нескольких строк DataFrame
print("Первые несколько строк DataFrame:")
print(df.head())

# Вычисление средней оценки по каждому предмету
mean_scores = df.mean(numeric_only=True)
print("\nСредняя оценка по каждому предмету:")
print(mean_scores)

# Вычисление медианной оценки по каждому предмету
median_scores = df.median(numeric_only=True)
print("\nМедианная оценка по каждому предмету:")
print(median_scores)

# Вычисление Q1 и Q3 для оценок по математике
Q1_math = df['Математика'].quantile(0.25)
Q3_math = df['Математика'].quantile(0.75)
print("\nQ1 и Q3 для оценок по математике:")
print("Q1 (25-й процентиль):", Q1_math)
print("Q3 (75-й процентиль):", Q3_math)

# Вычисление IQR (межквартильного размаха)
IQR_math = Q3_math - Q1_math
print("\nIQR для оценок по математике:")
print(IQR_math)

# Вычисление стандартного отклонения
std_dev = df.std(numeric_only=True)
print("\nСтандартное отклонение по каждому предмету:")
print(std_dev)